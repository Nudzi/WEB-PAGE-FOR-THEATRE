//fatima
document.getElementById("fatima").onclick = function () {
  window.onclick = function (event) {
    if (event.target == document.getElementById("myModalFatima")) {
      document.getElementById("myModalFatima").style.display = "none";
    }
  };
  document.getElementById("myModalFatima").style.display = "block";
  document.getElementById("cl4").onclick = function () {
    document.getElementById("myModalFatima").style.display = "none";
  };
};
